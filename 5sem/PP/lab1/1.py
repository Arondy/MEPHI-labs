import re

def extract_time_spent(log_text):
    # Регулярное выражение для поиска времени
    pattern = r"Time spent:\s+([\d.]+)"
    # Ищем все совпадения
    times = re.findall(pattern, log_text)
    # Преобразуем строки в числа с плавающей точкой
    for time in times:
        if time:
            print(time.replace('.', ','))

